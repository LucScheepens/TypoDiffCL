# ── AML pattern feature metadata ──────────────────────────────────────────────
# These are cols 11-18 in the 19-dim x_dense; col 0 = laundering flag.
PATTERN_COLS = list(range(11, 19))
PATTERN_NAMES = [
    "out_degree_norm",
    "in_degree_norm",
    "degree_asymmetry",
    "is_passthrough",
    "stack_depth_norm",
    "in_cycle",
    "scatter_gather_score",
    "bipartite_score",
]


# ── Data loading ───────────────────────────────────────────────────────────────

def load_cached_graphs(cache_path: Path):
    """
    Load the diffusion dataset cache produced by train.py.
    Returns list of (x [n,19], adj [n,n]) tensors.
    """
    print(f"Loading cache: {cache_path}")
    data = torch.load(str(cache_path), weights_only=False)
    print(f"  {len(data)} graphs loaded")
    return data


def extract_pattern_means(graphs) -> tuple[np.ndarray, np.ndarray]:
    """
    Split graphs into laundering / clean and return per-graph mean of
    AML pattern features (cols 11-18 of x_dense).

    Returns
    -------
    laund_means : [N_laund, 8]
    clean_means : [N_clean, 8]
    """
    laund, clean = [], []
    for x, _adj in graphs:
        is_laund = bool((x[:, 0] > 0.5).any().item())
        feats = x[:, PATTERN_COLS].float().mean(dim=0).numpy()  # [8]
        if is_laund:
            laund.append(feats)
        else:
            clean.append(feats)
    laund_arr = np.stack(laund) if laund else np.zeros((0, 8))
    clean_arr = np.stack(clean) if clean else np.zeros((0, 8))
    print(f"  real laundering: {len(laund)}, real clean: {len(clean)}")
    return laund_arr, clean_arr


# ── Diffusion generation ───────────────────────────────────────────────────────

def generate_with_diffusion(ckpt_path: Path, n_gen: int, device: torch.device,
                            n_viz: int = 6,
                            max_nodes: int = 300):
    """
    Run the trained diffusion model in class-conditional mode (label=1, laundering)
    to produce n_gen synthetic graphs.

    Returns (gen_means, gen_samples) where:
      gen_means   : [n_gen, 8] per-graph AML pattern feature means
      gen_samples : list of (x0_np [n,18], adj_np [n,n], laund_pred [n]) for the
                    first n_viz generated graphs (for network visualisation)
    Returns None if the checkpoint is missing or generation fails.
    """
    if not ckpt_path.exists():
        print(f"  [SKIP] Diffusion checkpoint not found: {ckpt_path}")
        return None

    try:
        from diffusion.model import DiffusionGNN
        from diffusion.diff_util import create_diffusion
    except ImportError as e:
        print(f"  [SKIP] Cannot import diffusion modules: {e}")
        return None

    print(f"  Loading diffusion model from {ckpt_path.name} …")
    ckpt = torch.load(str(ckpt_path), map_location=device, weights_only=False)
    node_dim    = ckpt["model"]["input_proj.weight"].shape[1]
    class_cond  = ckpt.get("class_conditional", False)
    deg_cond    = ckpt.get("degree_conditioning", "degree_proj.weight" in ckpt["model"])
    x_mean      = ckpt["x_mean"].to(device)
    x_std       = ckpt["x_std"].to(device)

    model = DiffusionGNN(node_dim=node_dim, hidden_dim=128, num_layers=4,
                         class_conditional=class_cond,
                         degree_conditioning=deg_cond).to(device)
    model.load_state_dict(ckpt["model"])
    model.eval()
    diffusion = create_diffusion(T=500)

    print(f"  Generating {n_gen} laundering graphs …")
    gen_means   = []
    gen_samples = []   # (x0_np, adj_np, laund_pred) for first n_viz
    T = 500

    with torch.no_grad():
        for idx in range(n_gen):
            n = np.random.randint(8, 40)  # realistic subgraph sizes

            # Start from Gaussian noise
            x_t   = torch.randn(1, max_nodes, node_dim, device=device)
            adj_t = torch.sigmoid(torch.randn(1, max_nodes, max_nodes, device=device))
            mask  = torch.zeros(1, max_nodes, device=device)
            mask[0, :n] = 1.0

            # Class label = 1 (laundering)
            label = torch.tensor([1], device=device) if class_cond else None

            # Single-step denoising from t=T//2 for speed (not full reverse chain)
            t_val = T // 2
            t     = torch.tensor([t_val], device=device)

            x_t_norm = x_t.clone()
            x_t_norm[:, :, 1:] = (x_t[:, :, 1:] - x_mean[1:]) / x_std[1:]
            x_t_norm = x_t_norm * mask.unsqueeze(-1)

            eps_pred, adj_pred, _ = model(
                x_t_norm, diffusion._scale_timesteps(t),
                adj=adj_t, node_mask=mask, class_labels=label,
            )

            # Predict x0 from eps
            ab = float(diffusion.alphas_cumprod[t_val])
            x0 = (x_t_norm[0, :n, 1:] - (1 - ab) ** 0.5 * eps_pred[0, :n, 1:]) / max(ab ** 0.5, 1e-8)
            x0 = x0 * x_std[1:] + x_mean[1:]   # denormalise continuous features

            # AML pattern features are at cols 10-17 of the 18 continuous dims
            # (cols 11-18 of 19-dim x_dense → indices 10-17 after stripping col 0)
            pattern_feats = x0[:, 10:18].cpu().float().mean(dim=0).numpy()
            gen_means.append(pattern_feats)

            # Collect full graph for visualisation
            if len(gen_samples) < n_viz:
                adj_np = adj_pred[0, :n, :n].cpu().numpy()
                adj_np = np.maximum(adj_np, adj_np.T)   # symmetrise for display
                np.fill_diagonal(adj_np, 0)
                # Single-step from t=250: model is still uncertain, use 0.5 so
                # only edges with majority probability are shown (0.35 would keep
                # ~65% of all pairs at high noise levels → unrealistic density).
                adj_bin = (adj_np > 0.5).astype(float)
                laund_pred = eps_pred[0, :n, 0].cpu().clamp(0, 1).numpy()
                gen_samples.append((x0.cpu().float().numpy(), adj_bin, laund_pred))

            if (idx + 1) % 50 == 0:
                print(f"    {idx + 1}/{n_gen} generated")

    return np.stack(gen_means), gen_samples


# ── Divergence metrics ─────────────────────────────────────────────────────────

def _histogram_kl(p: np.ndarray, q: np.ndarray, bins: int = 30) -> float:
    """KL divergence D_KL(P||Q) estimated from samples via histogram."""
    lo, hi = min(p.min(), q.min()), max(p.max(), q.max())
    if hi <= lo:
        return 0.0
    edges = np.linspace(lo, hi, bins + 1)
    hp, _ = np.histogram(p, bins=edges, density=True)
    hq, _ = np.histogram(q, bins=edges, density=True)
    eps = 1e-10
    hp = hp + eps; hq = hq + eps
    hp /= hp.sum(); hq /= hq.sum()
    return float(np.sum(hp * np.log(hp / hq)))


def _wasserstein1(p: np.ndarray, q: np.ndarray) -> float:
    """Wasserstein-1 (Earth Mover's Distance) for 1-D distributions."""
    ps = np.sort(p)
    qs = np.sort(q)
    # Interpolate to common grid
    n = max(len(ps), len(qs))
    p_interp = np.interp(np.linspace(0, 1, n), np.linspace(0, 1, len(ps)), ps)
    q_interp = np.interp(np.linspace(0, 1, n), np.linspace(0, 1, len(qs)), qs)
    return float(np.mean(np.abs(p_interp - q_interp)))


def compute_divergences(laund: np.ndarray, clean: np.ndarray,
                        gen: np.ndarray | None) -> list[dict]:
    """Compute KL and Wasserstein for each AML pattern feature."""
    rows = []
    for i, name in enumerate(PATTERN_NAMES):
        p = laund[:, i]
        q = clean[:, i]
        row = {
            "feature": name,
            "kl_laund_vs_clean": _histogram_kl(p, q),
            "w1_laund_vs_clean": _wasserstein1(p, q),
            "mean_real_laund": float(p.mean()),
            "mean_real_clean": float(q.mean()),
        }
        if gen is not None and len(gen) > 0:
            g = gen[:, i]
            row["kl_laund_vs_gen"]    = _histogram_kl(p, g)
            row["w1_laund_vs_gen"]    = _wasserstein1(p, g)
            row["mean_gen_laund"]     = float(g.mean())
        rows.append(row)
    return rows


# ── Plotting ──────────────────────────────────────────────────────────────────

def plot_distributions(laund: np.ndarray, clean: np.ndarray,
                       gen: np.ndarray | None, out_path: Path):
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
    except ImportError:
        print("  [SKIP] matplotlib not available — skipping plots")
        return

    n_feats = len(PATTERN_NAMES)
    ncols = 4
    nrows = (n_feats + ncols - 1) // ncols
    fig, axes = plt.subplots(nrows, ncols, figsize=(ncols * 4, nrows * 3))
    axes = axes.flatten()

    bins = 30
    for i, (name, ax) in enumerate(zip(PATTERN_NAMES, axes)):
        lo = min(laund[:, i].min(), clean[:, i].min())
        hi = max(laund[:, i].max(), clean[:, i].max())
        if gen is not None:
            lo = min(lo, gen[:, i].min())
            hi = max(hi, gen[:, i].max())
        edges = np.linspace(lo - 1e-8, hi + 1e-8, bins + 1)

        ax.hist(clean[:, i],  bins=edges, density=True, alpha=0.45,
                color="#4a90d9", label="real clean")
        ax.hist(laund[:, i],  bins=edges, density=True, alpha=0.55,
                color="#e05c2e", label="real laund")
        if gen is not None and len(gen) > 0:
            ax.hist(gen[:, i], bins=edges, density=True, alpha=0.45,
                    color="#2ecc71", label="gen laund", linestyle="--",
                    histtype="step", linewidth=1.5)
        ax.set_title(name.replace("_", " "), fontsize=9)
        ax.set_xlabel("node mean value", fontsize=7)
        ax.set_ylabel("density", fontsize=7)
        ax.tick_params(labelsize=7)
        if i == 0:
            ax.legend(fontsize=7)

    # Hide unused axes
    for ax in axes[n_feats:]:
        ax.set_visible(False)

    fig.suptitle("AML Pattern Feature Distributions\n"
                 "real clean vs real laundering vs generated laundering",
                 fontsize=11, y=1.01)
    fig.tight_layout()
    out_path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(str(out_path), dpi=150, bbox_inches="tight")
    print(f"  Plot saved → {out_path}")
    plt.close(fig)


def extract_sample_graphs(graphs, n_samples: int, laundering: bool) -> list:
    """
    Pick n_samples random (x_np [n,19], adj_np [n,n]) pairs from the real graph
    list, filtered to laundering or clean graphs.
    """
    pool = []
    for x, adj in graphs:
        is_laund = bool((x[:, 0] > 0.5).any().item())
        if is_laund == laundering:
            adj_np = adj.numpy() if isinstance(adj, torch.Tensor) else adj
            x_np   = x.numpy()  if isinstance(x,   torch.Tensor) else x
            adj_sym = np.maximum(adj_np, adj_np.T)
            np.fill_diagonal(adj_sym, 0)
            pool.append((x_np, adj_sym))
    rng   = np.random.default_rng(42)
    picks = rng.choice(len(pool), min(n_samples, len(pool)), replace=False)
    return [pool[i] for i in picks]


def plot_network_gallery(laund_samples, clean_samples, gen_samples,
                         out_path: Path, n_viz: int):
    """
    Three-row gallery: Real Laundering / Real Clean / Generated.
    Each column is one example network.
    """
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
        import matplotlib.patches as mpatches
        import networkx as nx
    except ImportError:
        print("  [SKIP] matplotlib / networkx not available — skipping gallery")
        return

    rows_def = [
        ("Real Laundering",       laund_samples, "#e05c2e"),
        ("Real Clean",            clean_samples, "#4a90d9"),
        ("Generated (Laund.)",    gen_samples,   "#27ae60"),
    ]
    rows = [(lbl, samps, col) for lbl, samps, col in rows_def if samps]
    if not rows:
        return

    ncols = max(len(s) for _, s, _ in rows)
    nrows = len(rows)
    fig, axes = plt.subplots(nrows, ncols, figsize=(ncols * 3.2, nrows * 3.2))
    if nrows == 1:
        axes = np.array([axes])
    if ncols == 1:
        axes = np.array([[ax] for ax in axes])

    for row_i, (row_label, samples, row_color) in enumerate(rows):
        is_gen = row_label.startswith("Generated")
        for col_i in range(ncols):
            ax = axes[row_i, col_i]
            if col_i >= len(samples):
                ax.set_visible(False)
                continue

            if is_gen:
                x_np, adj_np, laund_pred = samples[col_i]
                n = x_np.shape[0]
                # Colour by model's per-node laundering prediction
                colors = ["#e74c3c" if laund_pred[k] > 0.5 else row_color
                          for k in range(n)]
            else:
                x_np, adj_np = samples[col_i]
                n = x_np.shape[0]
                laund_flag = x_np[:, 0] > 0.5
                colors = ["#e74c3c" if laund_flag[k] else "#aed6f1"
                          for k in range(n)]

            G = nx.Graph()
            G.add_nodes_from(range(n))
            for a in range(n):
                for b in range(a + 1, n):
                    if adj_np[a, b] > 0.5:
                        G.add_edge(a, b)

            ne       = G.number_of_edges()
            dens     = ne / max(n * (n - 1) / 2, 1)
            degs     = [d for _, d in G.degree()]
            mean_deg = float(np.mean(degs)) if degs else 0.0

            try:
                pos = nx.spring_layout(G, seed=42,
                                       k=2.5 / max(n ** 0.5, 1), iterations=50)
            except Exception:
                pos = nx.random_layout(G, seed=42)

            node_sizes = np.array([max(40, 20 * (G.degree(v) + 1)) for v in range(n)])
            node_sizes = np.clip(node_sizes, 30, 300).tolist()

            nx.draw_networkx_nodes(G, pos, ax=ax, node_color=colors,
                                   node_size=node_sizes, alpha=0.90)
            nx.draw_networkx_edges(G, pos, ax=ax, edge_color="#777777",
                                   width=0.7, alpha=0.50, arrows=False)

            stats = f"n={n}  e={ne}\ndens={dens:.3f}  deg={mean_deg:.1f}"
            if not is_gen:
                frac = float(laund_flag.mean())
                stats += f"\nlaund={frac:.0%}"
            ax.text(0.03, 0.03, stats, transform=ax.transAxes, fontsize=7,
                    va="bottom",
                    bbox=dict(boxstyle="round,pad=0.3", fc="white",
                              ec="#cccccc", alpha=0.88))
            ax.axis("off")

            if col_i == 0:
                ax.set_ylabel(row_label, fontsize=9, fontweight="bold",
                              color=row_color, labelpad=6)

    legend_handles = [
        mpatches.Patch(color="#e74c3c", label="Laundering node"),
        mpatches.Patch(color="#aed6f1", label="Clean node"),
        mpatches.Patch(color="#27ae60", label="Generated node (clean pred.)"),
    ]
    fig.legend(handles=legend_handles, loc="lower center", ncol=3,
               fontsize=8, framealpha=0.9, bbox_to_anchor=(0.5, 0.0))
    fig.suptitle(
        f"Network Gallery — Real vs Generated  ({n_viz} samples per row)",
        fontsize=11, y=1.01,
    )
    fig.tight_layout(rect=[0, 0.04, 1, 1])
    out_path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(str(out_path), dpi=150, bbox_inches="tight")
    print(f"  Network gallery saved → {out_path}")
    plt.close(fig)


def save_kl_csv(rows: list[dict], out_path: Path):
    out_path.parent.mkdir(parents=True, exist_ok=True)
    fieldnames = list(rows[0].keys())
    with open(out_path, "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)
    print(f"  KL table saved → {out_path}")


def print_summary(rows: list[dict]):
    has_gen = "kl_laund_vs_gen" in rows[0]
    header = f"{'Feature':<25} {'KL(L||C)':>10} {'W1(L,C)':>10}"
    if has_gen:
        header += f" {'KL(L||G)':>10} {'W1(L,G)':>10}"
    print("\n" + "─" * len(header))
    print("AML Pattern Distribution Divergences")
    print("  L = real laundering  C = real clean  G = generated laundering")
    print("─" * len(header))
    print(header)
    print("─" * len(header))
    for r in rows:
        line = f"{r['feature']:<25} {r['kl_laund_vs_clean']:>10.4f} {r['w1_laund_vs_clean']:>10.4f}"
        if has_gen:
            line += f" {r.get('kl_laund_vs_gen', float('nan')):>10.4f}"
            line += f" {r.get('w1_laund_vs_gen', float('nan')):>10.4f}"
        print(line)
    print("─" * len(header))

    # Headline summary
    mean_kl_lc = np.mean([r["kl_laund_vs_clean"] for r in rows])
    print(f"\nMean KL(real_laund || real_clean): {mean_kl_lc:.4f}")
    if has_gen:
        mean_kl_lg = np.mean([r.get("kl_laund_vs_gen", 0) for r in rows])
        ratio = mean_kl_lg / max(mean_kl_lc, 1e-8)
        print(f"Mean KL(real_laund || gen_laund):  {mean_kl_lg:.4f}  "
              f"({ratio:.2f}× vs clean gap — lower is better)")
        if ratio < 0.5:
            print("  ✓ Generated graphs are substantially closer to real laundering "
                  "than clean graphs are.")
        elif ratio < 1.0:
            print("  ~ Generated graphs partially preserve AML pattern structure.")
        else:
            print("  ✗ Generated graphs deviate from real laundering more than "
                  "clean graphs do — generation quality needs improvement.")


# ── Main ──────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(description=__doc__,
                                     formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("--cache", type=str,
                        default=str(ROOT_DIR / "data" / "cached_dataset_LI-Small_Trans.pt"),
                        help="Path to diffusion dataset cache (list of (x,adj) pairs)")
    parser.add_argument("--diff-ckpt", type=str,
                        default=str(ROOT_DIR / "checkpoints" / "LI-Small" / "diffusion" / "model.pt"),
                        help="Path to trained diffusion model checkpoint")
    parser.add_argument("--n-gen", type=int, default=200,
                        help="Number of graphs to generate (default 200)")
    parser.add_argument("--n-viz", type=int, default=6,
                        help="Number of example networks per row in the gallery (default 6)")
    parser.add_argument("--out", type=str,
                        default=str(_HERE / "results"),
                        help="Output directory for plots and CSV")
    parser.add_argument("--no-gen", action="store_true",
                        help="Skip generation (compare real laund vs real clean only)")
    args = parser.parse_args()

    cache_path  = Path(args.cache)
    diff_ckpt   = Path(args.diff_ckpt)
    out_dir     = Path(args.out)
    device      = torch.device("cuda" if torch.cuda.is_available() else "cpu")

    print(f"Device : {device}")
    print(f"Cache  : {cache_path}")
    print(f"Output : {out_dir}\n")

    # ── 1. Load real graphs ────────────────────────────────────────────────
    if not cache_path.exists():
        raise FileNotFoundError(
            f"Dataset cache not found: {cache_path}\n"
            f"Run train.py first (phase=diffusion) to build the cache, or point\n"
            f"--cache at an existing cached_dataset_*.pt file."
        )

    graphs = load_cached_graphs(cache_path)
    laund_means, clean_means = extract_pattern_means(graphs)

    if len(laund_means) == 0:
        raise RuntimeError("No laundering graphs found in cache.")

    # ── 2. Generate graphs ─────────────────────────────────────────────────
    gen_means   = None
    gen_samples = []
    if not args.no_gen:
        result = generate_with_diffusion(diff_ckpt, args.n_gen, device,
                                         n_viz=args.n_viz)
        if result is not None:
            gen_means, gen_samples = result

    # ── 3. Compute divergences ─────────────────────────────────────────────
    rows = compute_divergences(laund_means, clean_means, gen_means)
    print_summary(rows)

    # ── 4. Save outputs ────────────────────────────────────────────────────
    plot_distributions(
        laund_means, clean_means, gen_means,
        out_dir / "pattern_shift_distributions.png",
    )
    save_kl_csv(rows, out_dir / "pattern_shift_kl.csv")

    # ── 5. Network gallery ─────────────────────────────────────────────────
    print("\nBuilding network gallery …")
    laund_samples = extract_sample_graphs(graphs, args.n_viz, laundering=True)
    clean_samples = extract_sample_graphs(graphs, args.n_viz, laundering=False)
    plot_network_gallery(
        laund_samples, clean_samples, gen_samples,
        out_dir / "pattern_shift_networks.png",
        n_viz=args.n_viz,
    )
    print("\nDone.")


if __name__ == "__main__":
    main()
