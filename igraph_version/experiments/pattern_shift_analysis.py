"""
pattern_shift_analysis.py
─────────────────────────
Experiment 1: do diffusion-generated graphs preserve real AML pattern structure?

Compares per-feature distributions of the 8 AML typological pattern features
(cols 11-18 in the 19-dim node feature tensor) across three groups:

  real_laund   — real laundering ego-subgraphs from LI-Small
  real_clean   — real clean (non-laundering) ego-subgraphs from LI-Small
  gen_laund    — graphs generated by the class-conditioned diffusion model
                 (skipped gracefully if no checkpoint is found)

For each group the per-graph feature vector is the mean across nodes, giving
one 8-dim point per graph.  This is compared via:
  - Per-feature histogram overlays
  - KL divergence: D_KL(real_laund || real_clean)
                   D_KL(real_laund || gen_laund)
  - Wasserstein-1 distance (same pairs)

Usage
─────
    # from igraph_version/ directory:
    python experiments/pattern_shift_analysis.py

    # or with explicit paths:
    python experiments/pattern_shift_analysis.py \\
        --cache data/cached_dataset_LI-Small_Trans.pt \\
        --diff-ckpt checkpoints/diffusion_ibm/model.pt \\
        --n-gen 200 \\
        --out experiments/results

Outputs
───────
    experiments/results/pattern_shift_distributions.png   — histogram grid
    experiments/results/pattern_shift_kl.csv              — KL + Wasserstein table
"""

