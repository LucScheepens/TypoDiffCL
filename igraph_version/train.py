def main():
    parser = argparse.ArgumentParser(description="AML graph generation — master training script")
    parser.add_argument("--dataset", choices=["ibm", "elliptic"], default=DATASET,
                        help=f"Dataset to train on (default: {DATASET})")
    parser.add_argument("--phase", choices=["diffusion", "simclr", "all"], default="all",
                        help="Which phase to run (default: all)")
    parser.add_argument("--ibm-csv", type=str, default=None,
                        help="Override IBM CSV path (overrides IBM_CSV_PATH in CONFIG)")
    args = parser.parse_args()

    if args.ibm_csv:
        global IBM_CSV_PATH
        IBM_CSV_PATH = args.ibm_csv

    dataset = args.dataset
    device  = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print(f"Device  : {device}")
    print(f"Dataset : {dataset}")
    print(f"Phase   : {args.phase}")

    if dataset == "ibm":
        if args.phase in ("diffusion", "all"):
            train_diffusion_ibm(device)
        if args.phase in ("simclr", "all"):
            train_simclr_ibm(device)
    else:
        if args.phase in ("diffusion", "all"):
            train_diffusion_elliptic(device)
        if args.phase in ("simclr", "all"):
            train_simclr_elliptic(device)

    print("\nTraining complete.")


if __name__ == "__main__":
    main()
