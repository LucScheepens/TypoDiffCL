def train_simclr_elliptic(device):
    from simclr import GraphEncoder, ProjectionHead, nt_xent_loss, sup_con_loss
    from torch_geometric.data import Data, Batch
    from diffusion.model     import DiffusionGNN
    from diffusion.diff_util import create_diffusion
    from data.elliptic_adapter import load_elliptic_pyg_graphs

    ckpt_dir  = CKPT_DIR / "simclr_elliptic"
    diff_ckpt = CKPT_DIR / "diffusion_elliptic" / "model.pt"
    ckpt_dir.mkdir(parents=True, exist_ok=True)

    print(f"\n{'='*60}")
    print(f"Elliptic SimCLR Training   ({SIMCLR_ELL_EPOCHS} epochs)")
    print(f"  max_nodes={ELLIPTIC_MAX_NODES}  depth={ELLIPTIC_DEPTH}")
    print(f"  Save : {ckpt_dir}")
    print('='*60)

    # ── load data ──────────────────────────────────────────────────────────────
    kwargs = dict(max_nodes=ELLIPTIC_MAX_NODES, depth=ELLIPTIC_DEPTH)
    if ELLIPTIC_DATA_DIR:
        kwargs["data_dir"] = ELLIPTIC_DATA_DIR
    raw_graphs = load_elliptic_pyg_graphs(**kwargs)

    def _extend_6d(g):
        n = g.x.shape[0]
        lc = torch.full((n, 1), float(g.y.item()))
        x6 = torch.cat([lc, g.x], dim=1)
        return Data(x=x6, edge_index=g.edge_index.clone(), y=g.y.clone())

    graphs = [_extend_6d(g) for g in raw_graphs]
    n_ill  = sum(g.y.item() == 1 for g in graphs)
    print(f"  {len(graphs)} graphs ({n_ill} illicit, {len(graphs)-n_ill} licit)")

    # ── load optional diffusion model ──────────────────────────────────────────
    diff_model = diffusion = x_mean = x_std = None
    if diff_ckpt.exists():
        ckpt       = torch.load(str(diff_ckpt), map_location=device, weights_only=False)
        diff_model = DiffusionGNN(node_dim=6, hidden_dim=DIFF_ELL_HIDDEN, num_layers=4).to(device)
        diff_model.load_state_dict(ckpt["model"])
        diff_model.eval()
        diffusion = create_diffusion(T=DIFF_ELL_TIMESTEPS)
        x_mean    = ckpt["x_mean"].to(device)
        x_std     = ckpt["x_std"].to(device)
        print("  Diffusion augmentation enabled.")
    else:
        print(f"  Diffusion checkpoint not found at {diff_ckpt} — structural augmentation only.")

    # ── augmentations ──────────────────────────────────────────────────────────
    def _augment(g, p_edge=SIMCLR_ELL_P_EDGE_DROP, p_feat=SIMCLR_ELL_P_FEAT_MASK):
        x = g.x.clone()
        mask = torch.rand(x.shape[0], x.shape[1] - 1) > p_feat
        x[:, 1:] *= mask.float()
        ei = g.edge_index
        if ei.shape[1] > 0:
            n_edges  = ei.shape[1] // 2
            keep     = torch.rand(n_edges) > p_edge
            keep_idx = keep.repeat_interleave(2).nonzero(as_tuple=False).squeeze(1)
            if len(keep_idx) > 0:
                ei = ei[:, keep_idx]
            else:
                ei = torch.zeros(2, 0, dtype=torch.long)
        return Data(x=x, edge_index=ei, y=g.y.clone())

    def _diff_view(g):
        if diff_model is None:
            return None
        n = g.x.shape[0]
        x_pad  = torch.zeros(1, ELLIPTIC_MAX_NODES, 6, device=device)
        a_pad  = torch.zeros(1, ELLIPTIC_MAX_NODES, ELLIPTIC_MAX_NODES, device=device)
        msk    = torch.zeros(1, ELLIPTIC_MAX_NODES, device=device)
        x_pad[0, :n]     = g.x.to(device)
        msk[0, :n]       = 1.0
        ei = g.edge_index
        if ei.shape[1] > 0:
            valid = ei[0] != ei[1]
            s, d  = ei[0][valid], ei[1][valid]
            inb   = (s < n) & (d < n)
            a_pad[0, s[inb], d[inb]] = 1.0
        x_n = x_pad.clone()
        x_n[:, :, 1:] = (x_pad[:, :, 1:] - x_mean[1:]) / x_std[1:]
        x_n = x_n * msk.unsqueeze(-1)
        t_frac = random.uniform(0.1, 0.4)
        t_val  = max(1, int(t_frac * DIFF_ELL_TIMESTEPS))
        t      = torch.tensor([t_val], device=device)
        x_t, adj_t = diffusion.q_sample(x_n, t, node_mask=msk, adj_start=a_pad)
        try:
            with torch.no_grad():
                ep, ap, _ = diff_model(x_t, diffusion._scale_timesteps(t),
                                       adj=adj_t, node_mask=msk)
                x0_c = diffusion._predict_xstart_from_eps(x_t[..., 1:], t, ep[..., 1:])
                x0_b = ep[..., 0:1].clamp(0.0, 1.0)
            x0     = torch.cat([x0_b, x0_c], dim=-1)[0, :n].cpu()
            x0[:, 1:] = x0[:, 1:] * x_std[1:].cpu() + x_mean[1:].cpu()
            deg    = ap[0, :n, :n].cpu().sum(-1)
            x0[:, 1] = deg / deg.max().clamp(min=1.0)
            ei_out = (ap[0, :n, :n].cpu() > 0.5).nonzero(as_tuple=False).T.contiguous()
            return Data(x=x0, edge_index=ei_out, y=g.y.clone())
        except Exception:
            return None

    # ── training loop ──────────────────────────────────────────────────────────
    encoder   = GraphEncoder(in_dim=6, hidden_dim=64, out_dim=128).to(device)
    projector = ProjectionHead(in_dim=128, proj_dim=64).to(device)
    optimizer = torch.optim.Adam(
        list(encoder.parameters()) + list(projector.parameters()), lr=SIMCLR_ELL_LR
    )

    best_loss = float("inf")
    t0        = time.time()

    for epoch in range(SIMCLR_ELL_EPOCHS):
        encoder.train(); projector.train()
        if diff_model: diff_model.eval()
        random.shuffle(graphs)
        total = nx_t = sc_t = 0.0; nb = 0

        for i in range(0, len(graphs), SIMCLR_ELL_BATCH):
            batch = graphs[i : i + SIMCLR_ELL_BATCH]
            v1, v2, lbls = [], [], []
            for g in batch:
                v1.append(_augment(g))
                if diff_model and random.random() < SIMCLR_ELL_P_DIFFUSION:
                    dv = _diff_view(g)
                    v2.append(dv if dv is not None else _augment(g))
                else:
                    v2.append(_augment(g))
                lbls.append(int(g.y.item()))

            labels = torch.tensor(lbls, dtype=torch.long, device=device)
            b1 = Batch.from_data_list(v1).to(device)
            b2 = Batch.from_data_list(v2).to(device)

            optimizer.zero_grad()
            h1 = encoder(b1); h2 = encoder(b2)
            z1 = projector(h1); z2 = projector(h2)
            ntxent = nt_xent_loss(z1, z2)
            z_all  = torch.cat([z1, z2]); lbl_all = torch.cat([labels, labels])
            sc     = sup_con_loss(z_all, lbl_all, temperature=SIMCLR_ELL_SUPCON_T)
            loss   = ntxent + SIMCLR_ELL_SUPCON_W * sc
            loss.backward(); optimizer.step()

            total += loss.item(); nx_t += ntxent.item()
            sc_t  += sc.item() if isinstance(sc, torch.Tensor) else float(sc)
            nb    += 1

        avg = total / max(nb, 1)
        print(f"Epoch {epoch+1:3d}/{SIMCLR_ELL_EPOCHS} | "
              f"loss={avg:.4f} (nt_xent={nx_t/max(nb,1):.4f}, supcon={sc_t/max(nb,1):.4f}) | "
              f"{time.time()-t0:.1f}s")

        if (epoch + 1) % SIMCLR_ELL_CKPT_EVERY == 0:
            torch.save({"encoder_state_dict": encoder.state_dict(),
                        "projector_state_dict": projector.state_dict(),
                        "loss": avg, "epoch": epoch + 1},
                       ckpt_dir / f"epoch_{epoch+1}.pt")

        if avg < best_loss:
            best_loss = avg
            torch.save({"encoder_state_dict": encoder.state_dict(),
                        "projector_state_dict": projector.state_dict(),
                        "loss": avg, "epoch": epoch + 1},
                       ckpt_dir / "best_model.pt")

    print(f"\nElliptic SimCLR checkpoints saved → {ckpt_dir}  (best loss={best_loss:.4f})")


