def train_diffusion_ibm(device):
    from diffusion.collate   import collate_fn
    from diffusion.dataset   import CachedDataset
    from diffusion.diff_util import create_diffusion, preprocess
    from diffusion.model     import DiffusionGNN
    from diffusion.resample  import LossSecondMomentResampler
    from diffusion.visualizations import run_all_visualizations
    from augmentation        import build_igraph_from_transactions
    from util import (preprocess_df, extract_networks_igraph)

    csv_stem   = Path(IBM_CSV_PATH).stem
    cache_path = DATA_DIR / f"cached_dataset_{csv_stem}.pt"
    graphs_dir = str(DIFF_DIR / "graphs")
    out_path   = IBM_DIFF_CKPT_DIR / "model.pt"
    out_path.parent.mkdir(parents=True, exist_ok=True)

    print(f"\n{'='*60}")
    print(f"IBM Diffusion Training   ({DIFF_IBM_EPOCHS} epochs)")
    print(f"  CSV  : {IBM_CSV_PATH}")
    print(f"  Save : {out_path}")
    print('='*60)

    NODE_DIM = 19  # laundering flag (1) + topology (5) + transaction (5) + AML patterns (8)

    def _needs_rebuild():
        if not cache_path.exists():
            return True
        s = torch.load(str(cache_path), weights_only=False)
        if len(s) == 0 or s[0][0].shape[1] != NODE_DIM:
            cache_path.unlink()
            return True
        return False

    simclr_cache = DATA_DIR / f"simclr_networks_cache_{csv_stem}.pt"

    if _needs_rebuild():
        print("Extracting IBM networks …")
        df_full  = preprocess_df(IBM_CSV_PATH)
        networks = extract_networks_igraph(
            df_full, max_depth=IBM_MAX_DEPTH,
            max_networks=IBM_MAX_NETWORKS * 2,
            collapse_threshold=IBM_COLLAPSE_THRESH,
            max_nodes=IBM_MAX_NODES,
        )
        for net in networks:
            net["graph"] = build_igraph_from_transactions(net["transactions"])
        print(f"  {len(networks)} networks extracted. Preprocessing …")

        # Cache raw networks for SimCLR training so it skips re-extraction
        if not simclr_cache.exists():
            torch.save(networks, str(simclr_cache))
            print(f"  Saved raw network cache → {simclr_cache}")

        preprocess(networks, save_path=str(cache_path))

    print(f"Loading cache: {cache_path}")
    dataset  = CachedDataset(str(cache_path))
    loader   = DataLoader(dataset, batch_size=DIFF_IBM_BATCH,
                          shuffle=True, collate_fn=collate_fn, drop_last=True)
    all_x    = torch.cat([x for x, _adj in dataset], dim=0)
    x_mean   = torch.zeros(NODE_DIM); x_std = torch.ones(NODE_DIM)
    x_mean[1:] = all_x[:, 1:].mean(0); x_std[1:] = all_x[:, 1:].std(0).clamp(min=1e-6)
    x_mean, x_std = x_mean.to(device), x_std.to(device)
    print(f"Dataset: {len(dataset)} graphs")

    model     = DiffusionGNN(node_dim=NODE_DIM, hidden_dim=DIFF_IBM_HIDDEN,
                             num_layers=DIFF_IBM_LAYERS,
                             class_conditional=DIFF_IBM_CLASS_COND,
                             degree_conditioning=True).to(device)
    diffusion = create_diffusion(T=DIFF_IBM_TIMESTEPS)
    resampler = LossSecondMomentResampler(diffusion)
    optimizer = torch.optim.Adam(model.parameters(), lr=DIFF_IBM_LR)

    # Option 3: load frozen SimCLR encoder for embedding-space alignment loss.
    # Only activated when a SimCLR checkpoint from a previous run already exists.
    frozen_enc_ibm = None
    simclr_ckpt_ibm = IBM_SIMCLR_CKPT_DIR / "best_model.pt"
    if DIFF_IBM_ALIGN_W > 0 and simclr_ckpt_ibm.exists():
        from simclr import GraphEncoder
        from torch_geometric.data import Data as _PygData, Batch as _PygBatch
        _sc = torch.load(str(simclr_ckpt_ibm), map_location=device, weights_only=False)
        _sd = _sc["encoder_state_dict"]
        from simclr import encoder_dims_from_state_dict as _edfs
        _in, _hid, _nl, _bn, _gin = _edfs(_sd)
        frozen_enc_ibm = GraphEncoder(in_dim=_in, hidden_dim=_hid, out_dim=128,
                                       n_layers=_nl, use_bn=_bn, use_gin=_gin).to(device)
        frozen_enc_ibm.load_state_dict(_sc["encoder_state_dict"])
        frozen_enc_ibm.eval()
        for _p in frozen_enc_ibm.parameters():
            _p.requires_grad_(False)
        print(f"  [Option 3] Alignment loss enabled (w={DIFF_IBM_ALIGN_W}) — "
              f"SimCLR ckpt loaded from {simclr_ckpt_ibm.name}")
    else:
        from torch_geometric.data import Data as _PygData, Batch as _PygBatch

    def _lr(ep):
        if ep < DIFF_IBM_WARMUP:
            return (ep + 1) / DIFF_IBM_WARMUP
        p = (ep - DIFF_IBM_WARMUP) / max(1, DIFF_IBM_EPOCHS - DIFF_IBM_WARMUP)
        return 0.5 * (1.0 + math.cos(math.pi * p))

    scheduler = torch.optim.lr_scheduler.LambdaLR(optimizer, _lr)
    use_amp   = (str(device) == "cuda")
    scaler    = torch.amp.GradScaler("cuda", enabled=use_amp)
    t0        = time.time()

    for epoch in range(DIFF_IBM_EPOCHS):
        model.train()
        total = 0.0; nb = 0

        for x_batch, adj_batch, mask in loader:
            x_batch   = x_batch.to(device)
            adj_batch = adj_batch.to(device)
            mask      = mask.to(device)
            n_nodes   = x_batch.shape[1]
            nb       += 1

            x_norm = x_batch.clone()
            x_norm[:, :, 1:] = (x_batch[:, :, 1:] - x_mean[1:]) / x_std[1:]
            x_norm = x_norm * mask.unsqueeze(-1)

            # Option 1: extract per-graph class labels from col-0 laundering flag
            if DIFF_IBM_CLASS_COND:
                graph_labels = ((x_batch[:, :, 0] * mask).sum(dim=1) > 0).long()
                null_mask    = torch.rand(graph_labels.shape[0], device=device) < DIFF_IBM_LABEL_DROP
                class_labels = graph_labels.masked_fill(null_mask, 2)   # 2 = CFG null token
            else:
                class_labels = None

            t, weights = resampler.sample(x_batch.shape[0], device)
            with torch.autocast("cuda", enabled=use_amp):
                x_t, adj_t = diffusion.q_sample(x_norm, t, node_mask=mask, adj_start=adj_batch)
                # Edge dropout: randomly zero some edges in the input adjacency so the
                # model cannot rely on a dense adj at every step.  This reduces
                # oversmoothing (all embeddings collapse → uniform adj_pred) and teaches
                # the model to predict reasonable adjacency even from sparser inputs.
                if DIFF_IBM_EDGE_DROP > 0:
                    drop = torch.bernoulli(
                        torch.full_like(adj_t, 1.0 - DIFF_IBM_EDGE_DROP)
                    )
                    adj_t_in = adj_t * drop
                else:
                    adj_t_in = adj_t
                eps_pred, adj_pred, node_pred = model(
                    x_t, diffusion._scale_timesteps(t),
                    adj=adj_t_in, node_mask=mask,
                    class_labels=class_labels,        # Option 1
                )
                # noise-pred loss on continuous features
                noise_true = x_t[:, :, 1:] - x_norm[:, :, 1:]
                mse = ((eps_pred[:, :, 1:] - noise_true) ** 2 * mask.unsqueeze(-1)).sum() / mask.sum().clamp(1)

            # BCE losses computed in float32 — bce is unsafe inside autocast
            eps_pred_f = eps_pred.float()
            adj_pred_f = adj_pred.float()
            mask_f     = mask.float()
            bce  = F.binary_cross_entropy(
                eps_pred_f[:, :, 0].clamp(1e-6, 1-1e-6) * mask_f,
                x_batch[:, :, 0].float() * mask_f, reduction="sum"
            ) / mask_f.sum().clamp(1)
            _m2 = mask_f[:, :, None] * mask_f[:, None, :]
            adj_l = F.binary_cross_entropy(
                adj_pred_f.clamp(1e-6, 1-1e-6) * _m2,
                adj_batch.float() * _m2, reduction="sum"
            ) / (_m2.sum()).clamp(1)
            pred_dens = adj_pred_f.sum(dim=(-1, -2)) / (n_nodes ** 2 + 1e-8)
            true_dens = adj_batch.float().sum(dim=(-1, -2)) / (n_nodes ** 2 + 1e-8)
            dens_loss = ((pred_dens - true_dens) ** 2).mean()

            # Connectivity penalty: every real node must have soft degree >= 1.
            # _m2 masks padding; sum over the last dim gives per-node soft degree.
            soft_deg  = (adj_pred_f * _m2).sum(dim=-1)           # [B, N]
            conn_loss = (F.relu(1.0 - soft_deg) * mask_f).sum() / mask_f.sum().clamp(1)

            # Option 3: embedding-space alignment — diffusion's predicted x0 should
            # land near the real graph in SimCLR embedding space.  Gradients flow
            # through eps_pred → model params; the frozen encoder contributes no
            # parameter updates.
            align_loss = torch.tensor(0.0, device=device)
            if frozen_enc_ibm is not None and nb % DIFF_IBM_ALIGN_EVERY == 0:
                ab_t_v  = torch.tensor(
                    [float(diffusion.alphas_cumprod[int(ti.item())]) for ti in t],
                    dtype=torch.float32, device=device,
                )
                sqrt_ab = ab_t_v.sqrt().view(-1, 1, 1).clamp(min=1e-8)
                sqrt_1m = (1.0 - ab_t_v).sqrt().view(-1, 1, 1)
                # Reconstruct x0_pred from eps_pred; grad flows through eps_pred_f
                x0_cont = (x_t[..., 1:].float().detach() - sqrt_1m * eps_pred_f[..., 1:]) / sqrt_ab
                x0_cont = x0_cont * x_std[1:] + x_mean[1:]      # denormalise
                pyg_pred_list, pyg_real_list = [], []
                _enc_in = frozen_enc_ibm.in_dim
                for bi in range(x_batch.shape[0]):
                    n_bi = int(mask[bi].sum().item())
                    if n_bi < 2:
                        continue
                    ei_pred = (adj_pred_f[bi, :n_bi, :n_bi].detach() > 0.5).nonzero(as_tuple=False).T.contiguous()
                    ei_real = (adj_batch[bi, :n_bi, :n_bi] > 0.5).nonzero(as_tuple=False).T.contiguous()
                    pyg_pred_list.append(_PygData(x=x0_cont[bi, :n_bi, :_enc_in], edge_index=ei_pred))
                    pyg_real_list.append(_PygData(x=x_batch[bi, :n_bi, 1:1+_enc_in].detach(), edge_index=ei_real))
                if pyg_pred_list:
                    bp = _PygBatch.from_data_list(pyg_pred_list)
                    br = _PygBatch.from_data_list(pyg_real_list)
                    h_pred = F.normalize(frozen_enc_ibm(bp), dim=-1)
                    with torch.no_grad():
                        h_real = F.normalize(frozen_enc_ibm(br), dim=-1)
                    align_loss = (1.0 - (h_pred * h_real.detach()).sum(dim=-1)).mean()

            loss = (mse + DIFF_IBM_LAUND_W   * bce
                    + DIFF_IBM_ADJ_W     * adj_l
                    + DIFF_IBM_DENSITY_W * dens_loss
                    + DIFF_IBM_CONN_W    * conn_loss
                    + DIFF_IBM_ALIGN_W   * align_loss)

            optimizer.zero_grad()
            scaler.scale(loss).backward()
            scaler.unscale_(optimizer)
            torch.nn.utils.clip_grad_norm_(model.parameters(), DIFF_IBM_GRAD_CLIP)
            scaler.step(optimizer); scaler.update()
            total += loss.item()

        scheduler.step()
        if (epoch + 1) % 10 == 0 or epoch < 3:
            print(f"Epoch {epoch+1:4d}/{DIFF_IBM_EPOCHS} | "
                  f"loss={total/max(nb,1):.4f} | lr={scheduler.get_last_lr()[0]:.2e} | "
                  f"{time.time()-t0:.0f}s")

        torch.save({"model": model.state_dict(),
                    "x_mean": x_mean.cpu(), "x_std": x_std.cpu(),
                    "class_conditional": DIFF_IBM_CLASS_COND,
                    "num_layers": DIFF_IBM_LAYERS,
                    "degree_conditioning": True}, out_path)

    print(f"\nIBM diffusion checkpoint saved → {out_path}")


