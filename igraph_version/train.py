def train_diffusion_ethereum(device):
    from diffusion.collate   import collate_fn
    from diffusion.diff_util import create_diffusion
    from diffusion.model     import DiffusionGNN
    from diffusion.resample  import LossSecondMomentResampler
    from data.ethereum_adapter import load_ethereum_pyg_graphs

    cache_path = DATA_DIR / "ethereum_diffusion_cache.pt"
    out_path   = CKPT_DIR / "diffusion_ethereum" / "model.pt"
    out_path.parent.mkdir(parents=True, exist_ok=True)

    print(f"\n{'='*60}")
    print(f"Ethereum Diffusion Training   ({DIFF_ETH_EPOCHS} epochs)")
    print(f"  max_nodes={ETHEREUM_MAX_NODES}  depth={ETHEREUM_DEPTH}")
    print(f"  Save : {out_path}")
    print('='*60)

    NODE_DIM = 6   # 1 label col + 5 structural features (same as Elliptic)

    def _pyg_to_dense(data):
        n = data.x.shape[0]
        x = torch.zeros(n, NODE_DIM)
        x[:, 0] = float(data.y.item())
        x[:, 1:] = data.x
        adj = torch.zeros(n, n)
        ei = data.edge_index
        if ei.shape[1] > 0:
            valid = ei[0] != ei[1]
            src, dst = ei[0][valid], ei[1][valid]
            inb = (src < n) & (dst < n)
            adj[src[inb], dst[inb]] = 1.0
        return x, adj

    if not cache_path.exists():
        kwargs = dict(max_nodes=ETHEREUM_MAX_NODES, depth=ETHEREUM_DEPTH)
        if ETHEREUM_PKL_PATH:
            kwargs["pkl_path"] = ETHEREUM_PKL_PATH
        raw = load_ethereum_pyg_graphs(**kwargs)
        pairs = [(x, a) for g in raw for x, a in [_pyg_to_dense(g)] if g.x.shape[0] >= 3]
        torch.save(pairs, str(cache_path))
        print(f"Saved {len(pairs)} dense pairs → {cache_path}")
    else:
        pairs = torch.load(str(cache_path), weights_only=False)
        if len(pairs) == 0 or pairs[0][0].shape[1] != NODE_DIM:
            cache_path.unlink()
            return train_diffusion_ethereum(device)

    pairs = [(x, a) for x, a in pairs if x.shape[0] <= ETHEREUM_MAX_NODES]
    print(f"Dataset: {len(pairs)} graphs")

    class _DS(Dataset):
        def __getitem__(self, i): return pairs[i]
        def __len__(self): return len(pairs)

    loader   = DataLoader(_DS(), batch_size=DIFF_ETH_BATCH,
                          shuffle=True, collate_fn=collate_fn, drop_last=True)
    all_x    = torch.cat([x for x, _ in pairs], dim=0)
    x_mean   = torch.zeros(NODE_DIM); x_std = torch.ones(NODE_DIM)
    x_mean[1:] = all_x[:, 1:].mean(0); x_std[1:] = all_x[:, 1:].std(0).clamp(min=1e-6)
    x_mean, x_std = x_mean.to(device), x_std.to(device)

    model     = DiffusionGNN(node_dim=NODE_DIM, hidden_dim=DIFF_ETH_HIDDEN,
                             num_layers=DIFF_ETH_LAYERS,
                             class_conditional=DIFF_ETH_CLASS_COND,
                             degree_conditioning=True).to(device)
    diffusion = create_diffusion(T=DIFF_ETH_TIMESTEPS)
    resampler = LossSecondMomentResampler(diffusion)
    optimizer = torch.optim.Adam(model.parameters(), lr=DIFF_ETH_LR)

    # Optional SimCLR alignment loss (active on second training pass)
    frozen_enc_eth = None
    simclr_ckpt_eth = CKPT_DIR / "simclr_ethereum" / "best_model.pt"
    if DIFF_ETH_ALIGN_W > 0 and simclr_ckpt_eth.exists():
        from simclr import GraphEncoder
        from torch_geometric.data import Data as _PygData, Batch as _PygBatch
        _sc = torch.load(str(simclr_ckpt_eth), map_location=device, weights_only=False)
        _sd = _sc["encoder_state_dict"]
        from simclr import encoder_dims_from_state_dict as _edfs_eth
        _in, _hid, _nl, _bn, _gin = _edfs_eth(_sd)
        frozen_enc_eth = GraphEncoder(in_dim=_in, hidden_dim=_hid, out_dim=128,
                                      n_layers=_nl, use_bn=_bn, use_gin=_gin).to(device)
        frozen_enc_eth.load_state_dict(_sd)
        frozen_enc_eth.eval()
        for _p in frozen_enc_eth.parameters():
            _p.requires_grad_(False)
        print(f"  [Alignment] SimCLR checkpoint loaded from {simclr_ckpt_eth.name}")
    else:
        from torch_geometric.data import Data as _PygData, Batch as _PygBatch

    def _lr(ep):
        if ep < DIFF_ETH_WARMUP:
            return (ep + 1) / DIFF_ETH_WARMUP
        p = (ep - DIFF_ETH_WARMUP) / max(1, DIFF_ETH_EPOCHS - DIFF_ETH_WARMUP)
        return 0.5 * (1.0 + math.cos(math.pi * p))

    scheduler = torch.optim.lr_scheduler.LambdaLR(optimizer, _lr)
    use_amp   = (str(device) == "cuda")
    scaler    = torch.amp.GradScaler("cuda", enabled=use_amp)
    t0        = time.time()

    for epoch in range(DIFF_ETH_EPOCHS):
        model.train()
        total = 0.0; nb = 0

        for x_batch, adj_batch, mask in loader:
            x_batch   = x_batch.to(device)
            adj_batch = adj_batch.to(device)
            mask      = mask.to(device)
            n_nodes   = x_batch.shape[1]
            nb       += 1

            x_norm = x_batch.clone()
            x_norm[:, :, 1:] = (x_batch[:, :, 1:] - x_mean[1:]) / x_std[1:]
            x_norm = x_norm * mask.unsqueeze(-1)

            if DIFF_ETH_CLASS_COND:
                graph_labels = ((x_batch[:, :, 0] * mask).sum(dim=1) > 0).long()
                null_mask    = torch.rand(graph_labels.shape[0], device=device) < DIFF_ETH_LABEL_DROP
                class_labels = graph_labels.masked_fill(null_mask, 2)
            else:
                class_labels = None

            t, weights = resampler.sample(x_batch.shape[0], device)
            with torch.autocast("cuda", enabled=use_amp):
                x_t, adj_t = diffusion.q_sample(x_norm, t, node_mask=mask, adj_start=adj_batch)
                if DIFF_ETH_EDGE_DROP > 0:
                    drop = torch.bernoulli(
                        torch.full_like(adj_t, 1.0 - DIFF_ETH_EDGE_DROP)
                    )
                    adj_t_in = adj_t * drop
                else:
                    adj_t_in = adj_t
                eps_pred, adj_pred, node_pred = model(
                    x_t, diffusion._scale_timesteps(t),
                    adj=adj_t_in, node_mask=mask,
                    class_labels=class_labels,
                )
                noise_true = x_t[:, :, 1:] - x_norm[:, :, 1:]
                mse = ((eps_pred[:, :, 1:] - noise_true) ** 2 * mask.unsqueeze(-1)).sum() / mask.sum().clamp(1)

            eps_pred_f = eps_pred.float()
            adj_pred_f = adj_pred.float()
            mask_f     = mask.float()
            bce  = F.binary_cross_entropy(
                eps_pred_f[:, :, 0].clamp(1e-6, 1-1e-6) * mask_f,
                x_batch[:, :, 0].float() * mask_f, reduction="sum"
            ) / mask_f.sum().clamp(1)
            _m2 = mask_f[:, :, None] * mask_f[:, None, :]
            adj_l = F.binary_cross_entropy(
                adj_pred_f.clamp(1e-6, 1-1e-6) * _m2,
                adj_batch.float() * _m2, reduction="sum"
            ) / (_m2.sum()).clamp(1)
            pred_dens = adj_pred_f.sum(dim=(-1, -2)) / (n_nodes ** 2 + 1e-8)
            true_dens = adj_batch.float().sum(dim=(-1, -2)) / (n_nodes ** 2 + 1e-8)
            dens_loss = ((pred_dens - true_dens) ** 2).mean()
            soft_deg  = (adj_pred_f * _m2).sum(dim=-1)
            conn_loss = (F.relu(1.0 - soft_deg) * mask_f).sum() / mask_f.sum().clamp(1)

            align_loss = torch.tensor(0.0, device=device)
            if frozen_enc_eth is not None and nb % DIFF_ETH_ALIGN_EVERY == 0:
                ab_t_v  = torch.tensor(
                    [float(diffusion.alphas_cumprod[int(ti.item())]) for ti in t],
                    dtype=torch.float32, device=device,
                )
                sqrt_ab = ab_t_v.sqrt().view(-1, 1, 1).clamp(min=1e-8)
                sqrt_1m = (1.0 - ab_t_v).sqrt().view(-1, 1, 1)
                x0_cont = (x_t[..., 1:].float().detach() - sqrt_1m * eps_pred_f[..., 1:]) / sqrt_ab
                x0_cont = x0_cont * x_std[1:] + x_mean[1:]
                pyg_pred_list, pyg_real_list = [], []
                for bi in range(x_batch.shape[0]):
                    n_bi = int(mask[bi].sum().item())
                    if n_bi < 2:
                        continue
                    ei_pred = (adj_pred_f[bi, :n_bi, :n_bi].detach() > 0.5).nonzero(as_tuple=False).T.contiguous()
                    ei_real = (adj_batch[bi, :n_bi, :n_bi] > 0.5).nonzero(as_tuple=False).T.contiguous()
                    pyg_pred_list.append(_PygData(x=x0_cont[bi, :n_bi], edge_index=ei_pred))
                    pyg_real_list.append(_PygData(x=x_batch[bi, :n_bi, 1:].detach(), edge_index=ei_real))
                if pyg_pred_list:
                    bp = _PygBatch.from_data_list(pyg_pred_list)
                    br = _PygBatch.from_data_list(pyg_real_list)
                    h_pred = F.normalize(frozen_enc_eth(bp), dim=-1)
                    with torch.no_grad():
                        h_real = F.normalize(frozen_enc_eth(br), dim=-1)
                    align_loss = (1.0 - (h_pred * h_real.detach()).sum(dim=-1)).mean()

            loss = (mse + DIFF_ETH_LAUND_W   * bce
                    + DIFF_ETH_ADJ_W     * adj_l
                    + DIFF_ETH_DENSITY_W * dens_loss
                    + DIFF_ETH_CONN_W    * conn_loss
                    + DIFF_ETH_ALIGN_W   * align_loss)

            optimizer.zero_grad()
            scaler.scale(loss).backward()
            scaler.unscale_(optimizer)
            torch.nn.utils.clip_grad_norm_(model.parameters(), DIFF_ETH_GRAD_CLIP)
            scaler.step(optimizer); scaler.update()
            total += loss.item()

        scheduler.step()
        if (epoch + 1) % 10 == 0 or epoch < 3:
            print(f"Epoch {epoch+1:4d}/{DIFF_ETH_EPOCHS} | "
                  f"loss={total/max(nb,1):.4f} | {time.time()-t0:.0f}s")

        torch.save({"model": model.state_dict(),
                    "x_mean": x_mean.cpu(), "x_std": x_std.cpu(),
                    "class_conditional": DIFF_ETH_CLASS_COND,
                    "num_layers": DIFF_ETH_LAYERS,
                    "degree_conditioning": True,
                    "max_nodes": ETHEREUM_MAX_NODES}, out_path)

    print(f"\nEthereum diffusion checkpoint saved → {out_path}")


