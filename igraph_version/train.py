def train_simclr_ibm(device):
    from simclr import GraphEncoder, ProjectionHead, train_simclr_fast
    from augmentation import build_igraph_from_transactions
    from util import (preprocess_df, extract_networks_igraph)
    from diffusion.model     import DiffusionGNN
    from diffusion.diff_util import create_diffusion

    ckpt_dir  = IBM_SIMCLR_CKPT_DIR
    diff_ckpt = IBM_DIFF_CKPT_DIR / "model.pt"
    csv_stem  = Path(IBM_CSV_PATH).stem
    cache_pt  = DATA_DIR / f"simclr_networks_cache_{csv_stem}.pt"
    ckpt_dir.mkdir(parents=True, exist_ok=True)

    print(f"\n{'='*60}")
    print(f"IBM SimCLR Training   ({SIMCLR_IBM_EPOCHS} epochs)")
    print(f"  CSV  : {IBM_CSV_PATH}")
    print(f"  Save : {ckpt_dir}")
    print('='*60)

    def _cache_stale(nets):
        """Return True if the cached networks have the wrong node feature dim."""
        for n in nets:
            xd = n.get("x_dense")
            if xd is not None:
                return xd.shape[1] != 19
        return False  # no x_dense yet — will be built on first use

    networks = None
    if cache_pt.exists():
        print(f"Loading networks from cache: {cache_pt}")
        networks = torch.load(str(cache_pt), weights_only=False)
        if _cache_stale(networks):
            print("  Stale cache (wrong node_dim) — rebuilding …")
            cache_pt.unlink()
            networks = None

    if networks is None:
        print("Extracting IBM networks …")
        df_full  = preprocess_df(IBM_CSV_PATH)
        networks = extract_networks_igraph(
            df_full, max_depth=IBM_MAX_DEPTH,
            max_networks=IBM_MAX_NETWORKS * 2,
            collapse_threshold=IBM_COLLAPSE_THRESH,
            max_nodes=IBM_MAX_NODES,
        )
        for net in networks:
            net["graph"] = build_igraph_from_transactions(net["transactions"])
        torch.save(networks, str(cache_pt))
        print(f"Saved cache → {cache_pt}")
    print(f"  {len(networks)} networks loaded")

    # Load optional diffusion model for augmentation
    diff_model = diffusion = x_mean = x_std = None
    if diff_ckpt.exists():
        ckpt            = torch.load(str(diff_ckpt), map_location=device, weights_only=False)
        node_dim_ckpt   = ckpt["model"]["input_proj.weight"].shape[1]
        class_cond_ckpt = ckpt.get("class_conditional", False)
        num_layers_ckpt = ckpt.get("num_layers", 4)   # fallback for old checkpoints
        deg_cond_ckpt   = ckpt.get("degree_conditioning",
                                    "degree_proj.weight" in ckpt["model"])
        diff_model = DiffusionGNN(node_dim=node_dim_ckpt, hidden_dim=DIFF_IBM_HIDDEN,
                                  num_layers=num_layers_ckpt,
                                  class_conditional=class_cond_ckpt,
                                  degree_conditioning=deg_cond_ckpt).to(device)
        diff_model.load_state_dict(ckpt["model"])
        diff_model.eval()
        diffusion = create_diffusion(T=DIFF_IBM_TIMESTEPS)
        x_mean    = ckpt["x_mean"].to(device)
        x_std     = ckpt["x_std"].to(device)
        print(f"  Diffusion augmentation enabled (node_dim={node_dim_ckpt}, "
              f"class_conditional={class_cond_ckpt}).")
    else:
        node_dim_ckpt = 11  # full feature dim; encoder will use node_dim_ckpt - 1
        print(f"  Diffusion checkpoint not found at {diff_ckpt} — training without augmentation.")

    # node_dim_ckpt is the diffusion model's full feature dim (includes col 0 = laundering flag).
    # The encoder receives col 0 stripped, so in_dim = node_dim_ckpt - 1.
    encoder   = GraphEncoder(in_dim=node_dim_ckpt - 1, hidden_dim=128, out_dim=128,
                             n_layers=3, use_bn=True).to(device)
    projector = ProjectionHead(in_dim=128, proj_dim=64).to(device)
    optimizer = torch.optim.Adam(
        list(encoder.parameters()) + list(projector.parameters()), lr=SIMCLR_IBM_LR
    )

    # Precompute full 19-dim x_dense for every network once before training so that
    # the inner loop's cache-miss branch (which calls compute_pattern_features) is
    # never hit during training — only the fast cache-hit path runs per batch.
    print("  Precomputing node features (19-dim) …")
    from diffusion.diff_util import network_to_dense as _ntd_pre
    for _net in networks:
        _xd = _net.get("x_dense")
        if _xd is None or _xd.shape[1] != 19:
            _x, _ = _ntd_pre(_net)
            _net["x_dense"] = _x
    print(f"  Precomputation done ({len(networks)} networks).")

    train_simclr_fast(
        networks=networks, full_df=None,
        encoder=encoder, projector=projector, optimizer=optimizer,
        device=device,
        batch_size=SIMCLR_IBM_BATCH,
        epochs=SIMCLR_IBM_EPOCHS,
        checkpoint_dir=str(ckpt_dir),
        diffusion_model=diff_model, diffusion=diffusion,
        x_mean=x_mean, x_std=x_std,
        p_diffusion=SIMCLR_IBM_P_DIFFUSION,
        diffusion_t_frac=SIMCLR_IBM_T_FRAC,
        max_nodes=IBM_MAX_NODES,
        probe_weight=SIMCLR_IBM_PROBE_W,
        ntxent_temperature=SIMCLR_IBM_NTXENT_T,
        supcon_weight=SIMCLR_IBM_SUPCON_W,
        supcon_temperature=SIMCLR_IBM_SUPCON_T,
        # Task-aware augmentation
        use_curriculum=True,
        curriculum_epochs=max(1, SIMCLR_IBM_EPOCHS // 2),
        use_motif_preserving=True,
        use_saliency=SIMCLR_IBM_PROBE_W > 0,
        saliency_update_interval=20,
        # Start with multi-step DDIM views so the encoder warms up before the
        # probe is fit; switch to guided views after probe_warmup_epochs.
        view_type="multistep_to_guided" if (diff_model is not None and SIMCLR_IBM_PROBE_W > 0) else "single_step",
        diff_n_steps=5,
        diff_t_start_frac=0.4,
        diff_guidance_scale=1.5,
        probe_warmup_epochs=max(20, SIMCLR_IBM_EPOCHS // 3),
    )
    print(f"\nIBM SimCLR checkpoints saved → {ckpt_dir}")


