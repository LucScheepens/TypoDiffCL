def train_simclr_ethereum(device):
    from simclr import GraphEncoder, ProjectionHead, OnlineProbeHead, nt_xent_loss, sup_con_loss, _is_connected_ei
    from torch_geometric.data import Data, Batch
    from diffusion.model     import DiffusionGNN
    from diffusion.diff_util import create_diffusion
    from data.ethereum_adapter import load_ethereum_pyg_graphs

    ckpt_dir  = CKPT_DIR / "simclr_ethereum"
    diff_ckpt = CKPT_DIR / "diffusion_ethereum" / "model.pt"
    ckpt_dir.mkdir(parents=True, exist_ok=True)

    print(f"\n{'='*60}")
    print(f"Ethereum SimCLR Training   ({SIMCLR_ETH_EPOCHS} epochs)")
    print(f"  max_nodes={ETHEREUM_MAX_NODES}  depth={ETHEREUM_DEPTH}")
    print(f"  Save : {ckpt_dir}")
    print('='*60)

    kwargs = dict(max_nodes=ETHEREUM_MAX_NODES, depth=ETHEREUM_DEPTH)
    if ETHEREUM_PKL_PATH:
        kwargs["pkl_path"] = ETHEREUM_PKL_PATH
    raw_graphs = load_ethereum_pyg_graphs(**kwargs)

    def _extend_6d(g):
        n = g.x.shape[0]
        lc = torch.full((n, 1), float(g.y.item()))
        x6 = torch.cat([lc, g.x], dim=1)
        return Data(x=x6, edge_index=g.edge_index.clone(), y=g.y.clone())

    graphs = [_extend_6d(g) for g in raw_graphs]
    n_ph   = sum(g.y.item() == 1 for g in graphs)
    print(f"  {len(graphs)} graphs ({n_ph} phishing, {len(graphs)-n_ph} clean)")

    diff_model = diffusion = x_mean = x_std = None
    if diff_ckpt.exists():
        ckpt            = torch.load(str(diff_ckpt), map_location=device, weights_only=False)
        class_cond_ckpt = ckpt.get("class_conditional", False)
        num_layers_ckpt = ckpt.get("num_layers", 3)
        deg_cond_ckpt   = ckpt.get("degree_conditioning", True)
        diff_model = DiffusionGNN(node_dim=6, hidden_dim=DIFF_ETH_HIDDEN,
                                  num_layers=num_layers_ckpt,
                                  class_conditional=class_cond_ckpt,
                                  degree_conditioning=deg_cond_ckpt).to(device)
        diff_model.load_state_dict(ckpt["model"])
        diff_model.eval()
        diffusion = create_diffusion(T=DIFF_ETH_TIMESTEPS)
        x_mean    = ckpt["x_mean"].to(device)
        x_std     = ckpt["x_std"].to(device)
        print(f"  Diffusion augmentation enabled (class_conditional={class_cond_ckpt}).")
    else:
        print(f"  Diffusion checkpoint not found — structural augmentation only.")

    def _augment(g, p_edge=SIMCLR_ETH_P_EDGE_DROP, p_feat=SIMCLR_ETH_P_FEAT_MASK):
        x  = g.x.clone()
        mask = torch.rand(x.shape[0], x.shape[1] - 1) > p_feat
        x[:, 1:] *= mask.float()
        ei = g.edge_index
        if ei.shape[1] > 0:
            n_edges = ei.shape[1] // 2
            keep     = torch.rand(n_edges) > p_edge
            keep_idx = keep.repeat_interleave(2).nonzero(as_tuple=False).squeeze(1)
            ei = ei[:, keep_idx] if len(keep_idx) > 0 else torch.zeros(2, 0, dtype=torch.long)
        return Data(x=x[:, 1:], edge_index=ei, y=g.y.clone())

    def _diff_view(g):
        if diff_model is None:
            return None
        n = g.x.shape[0]
        x_pad  = torch.zeros(1, ETHEREUM_MAX_NODES, 6, device=device)
        a_pad  = torch.zeros(1, ETHEREUM_MAX_NODES, ETHEREUM_MAX_NODES, device=device)
        msk    = torch.zeros(1, ETHEREUM_MAX_NODES, device=device)
        x_pad[0, :n]     = g.x.to(device)
        msk[0, :n]       = 1.0
        ei = g.edge_index
        if ei.shape[1] > 0:
            valid = ei[0] != ei[1]
            s, d  = ei[0][valid], ei[1][valid]
            inb   = (s < n) & (d < n)
            a_pad[0, s[inb], d[inb]] = 1.0
        x_n = x_pad.clone()
        x_n[:, :, 1:] = (x_pad[:, :, 1:] - x_mean[1:]) / x_std[1:]
        x_n = x_n * msk.unsqueeze(-1)
        t_frac = random.uniform(0.1, 0.4)
        t_val  = max(1, int(t_frac * DIFF_ETH_TIMESTEPS))
        t      = torch.tensor([t_val], device=device)
        x_t, adj_t = diffusion.q_sample(x_n, t, node_mask=msk, adj_start=a_pad)
        try:
            with torch.no_grad():
                ep, ap, _ = diff_model(x_t, diffusion._scale_timesteps(t),
                                       adj=adj_t, node_mask=msk)
                x0_c = diffusion._predict_xstart_from_eps(x_t[..., 1:], t, ep[..., 1:])
                x0_b = ep[..., 0:1].clamp(0.0, 1.0)
            x0     = torch.cat([x0_b, x0_c], dim=-1)[0, :n].cpu()
            x0[:, 1:] = x0[:, 1:] * x_std[1:].cpu() + x_mean[1:].cpu()
            deg    = ap[0, :n, :n].cpu().sum(-1)
            x0[:, 1] = deg / deg.max().clamp(min=1.0)
            ei_out = (ap[0, :n, :n].cpu() > 0.5).nonzero(as_tuple=False).T.contiguous()
            return Data(x=x0[:, 1:], edge_index=ei_out, y=g.y.clone())
        except Exception:
            return None

    # in_dim=5: label column stripped before encoder (label-leakage fix)
    encoder    = GraphEncoder(in_dim=5, hidden_dim=128, out_dim=128,
                              n_layers=3, use_bn=True).to(device)
    projector  = ProjectionHead(in_dim=128, proj_dim=64).to(device)
    eth_probe  = OnlineProbeHead(in_dim=128).to(device) if SIMCLR_ETH_PROBE_W > 0 else None
    all_params = (list(encoder.parameters()) + list(projector.parameters())
                  + (list(eth_probe.parameters()) if eth_probe else []))
    optimizer  = torch.optim.Adam(all_params, lr=SIMCLR_ETH_LR)

    best_loss = float("inf")
    t0        = time.time()

    for epoch in range(SIMCLR_ETH_EPOCHS):
        encoder.train(); projector.train()
        if eth_probe: eth_probe.train()
        if diff_model: diff_model.eval()
        random.shuffle(graphs)
        total = nx_t = sc_t = probe_t = 0.0; nb = 0

        for i in range(0, len(graphs), SIMCLR_ETH_BATCH):
            batch = graphs[i : i + SIMCLR_ETH_BATCH]
            v1, v2, lbls = [], [], []
            for g in batch:
                v1.append(_augment(g))
                if diff_model and random.random() < SIMCLR_ETH_P_DIFFUSION:
                    dv = _diff_view(g)
                    if dv is not None and not _is_connected_ei(dv.edge_index, dv.x.shape[0]):
                        dv = None
                    v2.append(dv if dv is not None else _augment(g))
                else:
                    v2.append(_augment(g))
                lbls.append(int(g.y.item()))

            labels = torch.tensor(lbls, dtype=torch.long, device=device)
            b1 = Batch.from_data_list(v1).to(device)
            b2 = Batch.from_data_list(v2).to(device)

            optimizer.zero_grad()
            h1 = encoder(b1); h2 = encoder(b2)
            z1 = projector(h1); z2 = projector(h2)
            ntxent  = nt_xent_loss(z1, z2, temperature=SIMCLR_ETH_NTXENT_T)
            z_all   = torch.cat([z1, z2]); lbl_all = torch.cat([labels, labels])
            sc      = sup_con_loss(z_all, lbl_all, temperature=SIMCLR_ETH_SUPCON_T)

            if eth_probe is not None:
                h_all       = torch.cat([h1.float(), h2.float()], dim=0)
                probe_logit = eth_probe(F.normalize(h_all, dim=-1))
                probe_loss  = F.binary_cross_entropy_with_logits(probe_logit, lbl_all.float())
            else:
                probe_loss = torch.tensor(0.0, device=device)

            loss = ntxent + SIMCLR_ETH_SUPCON_W * sc + SIMCLR_ETH_PROBE_W * probe_loss
            loss.backward(); optimizer.step()

            total   += loss.item()
            nx_t    += ntxent.item()
            sc_t    += sc.item() if isinstance(sc, torch.Tensor) else float(sc)
            probe_t += probe_loss.item()
            nb      += 1

        avg       = total   / max(nb, 1)
        probe_str = f", probe={probe_t/max(nb,1):.4f}" if eth_probe else ""
        print(f"Epoch {epoch+1:3d}/{SIMCLR_ETH_EPOCHS} | "
              f"loss={avg:.4f} (nt_xent={nx_t/max(nb,1):.4f}, "
              f"supcon={sc_t/max(nb,1):.4f}{probe_str}) | "
              f"{time.time()-t0:.1f}s")

        _ckpt_data = {
            "encoder_state_dict":   encoder.state_dict(),
            "projector_state_dict": projector.state_dict(),
            "probe_state_dict":     eth_probe.state_dict() if eth_probe else None,
            "loss": avg, "epoch": epoch + 1,
        }
        if (epoch + 1) % SIMCLR_ETH_CKPT_EVERY == 0:
            torch.save(_ckpt_data, ckpt_dir / f"epoch_{epoch+1}.pt")

        if avg < best_loss:
            best_loss = avg
            torch.save(_ckpt_data, ckpt_dir / "best_model.pt")

    print(f"\nEthereum SimCLR checkpoints saved → {ckpt_dir}  (best loss={best_loss:.4f})")


